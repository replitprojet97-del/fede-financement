let updated = 0;
let errors = 0;

// 1. Update web locale files
const webLocalePath = path.join(root, 'artifacts/financedom/src/i18n/locales');
for (const [lang, keys] of Object.entries(webLandingKeys)) {
  const filePath = path.join(webLocalePath, `${lang}.json`);
  if (!fs.existsSync(filePath)) { console.warn(`[web] SKIP missing: ${lang}.json`); continue; }
  try {
    const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));
    if (!data.landing) data.landing = {};
    Object.assign(data.landing, keys);
    fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf8');
    console.log(`[web] ✓ ${lang}.json — ${Object.keys(keys).length} landing keys`);
    updated++;
  } catch (e) { console.error(`[web] ✗ ${lang}.json:`, e.message); errors++; }
}

// 2. Update mobile locale files
const mobileLocalePath = path.join(root, 'artifacts/capsubvention-mobile/lib/i18n/locales');
for (const [lang, sections] of Object.entries(mobileNewKeys)) {
  const filePath = path.join(mobileLocalePath, `${lang}.json`);
  if (!fs.existsSync(filePath)) { console.warn(`[mobile] SKIP missing: ${lang}.json`); continue; }
  try {
    const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));
    for (const [section, keys] of Object.entries(sections)) {
      if (!data[section]) data[section] = {};
      Object.assign(data[section], keys);
    }
    fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf8');
    console.log(`[mobile] ✓ ${lang}.json`);
    updated++;
  } catch (e) { console.error(`[mobile] ✗ ${lang}.json:`, e.message); errors++; }
}

// 3. Create video locale files
const videoLocalePath = path.join(root, 'artifacts/capsubvention-video/src/i18n/locales');
