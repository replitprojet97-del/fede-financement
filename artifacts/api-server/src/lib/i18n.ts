export function t(
  lang: string | undefined | null,
  key: string,
  vars?: Record<string, string | number>,
): string {
  const l = ((lang as Lang) in Object.fromEntries(L.map((x) => [x, 1])) ? lang : "fr") as Lang;
  const dict = T[key];
  if (!dict) return key;
  const str = dict[l] ?? dict["fr"] ?? key;
  if (!vars) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? `{${k}}`));
}

export function isValidLang(l: string): l is Lang {
  return SUPPORTED_LANGS.includes(l as Lang);
}
